package model;

public class VoceOrdineBean {
	Integer id;
	Integer quantita;
	Double prezzo;
	Integer idOrdine;
	Integer idVariante_articolo;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getQuantita() {
		return quantita;
	}
	public void setQuantita(Integer quantita) {
		this.quantita = quantita;
	}
	public Double getPrezzo() {
		return prezzo;
	}
	public void setPrezzo(Double prezzo) {
		this.prezzo = prezzo;
	}
	public Integer getIdOrdine() {
		return idOrdine;
	}
	public void setIdOrdine(Integer idOrdine) {
		this.idOrdine = idOrdine;
	}
	public Integer getIdVarianteArticolo() {
		return idVariante_articolo;
	}
	public void setIdVarianteArticolo(Integer idVariante_articolo) {
		this.idVariante_articolo = idVariante_articolo;
	}
	

}
