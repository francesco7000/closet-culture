package model;

public class CarrelloBean {
	
	int variante_articolo;
	int id_utente;
	int quantita;

	public int getVariante_articolo() {
		return variante_articolo;
	}
	public void setVariante_articolo(int variante_articolo) {
		this.variante_articolo = variante_articolo;
	}
	public int getId_utente() {
		return id_utente;
	}
	public void setId_utente(int id_utente) {
		this.id_utente = id_utente;
	}
	public int getQuantita() {
		return quantita;
	}
	public void setQuantita(int quantita) {
		this.quantita = quantita;
	}

	
}
