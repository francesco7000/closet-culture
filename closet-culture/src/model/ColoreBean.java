package model;

public class ColoreBean {
	int id;
	String nome;
	String codice_html; 

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCodice_html() {
		return codice_html;
	}
	public void setCodice_html(String codice_html) {
		this.codice_html = codice_html;
	}

}
